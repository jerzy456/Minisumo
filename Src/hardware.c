		TIM1->CNT = (TIM1->ARR) / 2;
		TIM3->CNT = (TIM3->ARR) / 2;
		TIM4->CNT = (TIM4->ARR) / 2;
		TIM8->CNT = (TIM8->ARR) / 2;
